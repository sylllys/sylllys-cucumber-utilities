package io.github.sylllys.cucumber.utilities;

public class SuiteConfigDetails {

    public String getDir() {
        return dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }

    public String getEnv() {
        return env;
    }

    public void setEnv(String env) {
        this.env = env;
    }

    private String dir;
    private String env;

}
